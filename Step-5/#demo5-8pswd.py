# 三次机会录入密码，正确就结束


for i in range(3):  # 代码将循环三次
    pswd = input('录入密码: ')  # 让用户录入密码，并将密码复制给pswd（这里需要注意缩进）
    if pswd == '123':  # 进行判断，如果用户录入的字符为：123
        print('正确，已进入')  # 打印输出，录入正确（这里需要注意缩进）
        break  # 此时循环打断，因为已经正确，不需要再次输入了
    else:                   # 如果没有输入正确，则打印：‘再试一次’ 然后在循环内继续循环,计数：i+1
        print('再试一次')
else:  # 三次循环完成时，如果没有正确录入，则打印：三次机会已经用完，再见
    print("三次机会已经用完,再见")





